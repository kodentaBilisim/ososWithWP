    <h5>Özel Sektör Öğretmenleri Sendikası Online Aidat Ödeme Sistemi Kullanım ve Gizlilik Sözleşmesi</h5>

    <p>İşbu sözleşme, Özel Sektör Öğretmenleri Sendikası (bundan sonra "Sendika" olarak anılacaktır) tarafından sunulan
        online aidat ödeme sisteminin kullanımı ve gizlilik esaslarını düzenlemektedir. Üyeler, bu sözleşme kapsamında
        belirlenen kuralları kabul ederek online aidat ödeme sistemini kullanabilirler.</p>

    <strong style="margin-bottom:5px">Madde 1 - Amaç ve Kapsam</strong>
    <p>Bu sözleşmenin amacı, Sendika üyelerinin aidat ödemelerini kolaylaştırmak amacıyla sunulan online ödeme sistemine
        ilişkin usul ve esasları düzenlemektir. Sistem, üyelerin kredi/banka kartı kullanarak aidat ödemesi yapabilmelerini
        sağlar.</p>

    <strong style="margin-bottom:5px">Madde 2 - Online Aidat Ödeme Talimatı</strong>
    <p>Üyeler, sistem üzerinden kredi/banka kartı bilgilerini tanıtarak, ayın istedikleri gününde ve belirledikleri tutarda
        kredi
        kartlarından aidat çekilmesi için talimat verebilirler. Üye, dilediği zaman sisteme giriş yaparak talimatı iptal
        etme veya düzenleme hakkına sahiptir. Üyenin belirlediği aidatın ilk ödemesi seçtiği güne bakılmaksızın sisteme talimat verdiği sırada çekilir. O ay içerisinde ayrıca çekim yapılmaz.</p>

    <strong style="margin-bottom:5px">Madde 3 - Kart Bilgilerinin Güvenliği</strong>
    <p>Kredi/banka kartı bilgileri, Sendika tarafından kesinlikle saklanmayacak ve sadece ödeme işlemi esnasında
        kullanılacaktır.
        Tüm ödeme işlemleri, PAYTR ödeme altyapısı aracılığıyla, kredi/banka kartının bağlı olduğu banka ile güvenli
        bağlantılar
        üzerinden gerçekleştirilecektir.</p>

    <strong style="margin-bottom:5px">Madde 4 - Ödeme Bilgilendirmesi</strong>
    <p>Üye, ödeme talimatı verdiği günün bir önceki gününde, kartından belirttiği tutarın çekileceğine dair
        bilgilendirilecektir. Üye, bu bilgilendirme sonrası çekim işlemi yapılmadan önce sisteme giriş yaparak ödeme
        talebini iptal etme hakkına sahiptir.</p>

    <strong style="margin-bottom:5px">Madde 5 - Talimatın İptali ve Değiştirilmesi</strong>
    <p>Üye, dilediği zaman sisteme giriş yaparak verdiği ödeme talimatını iptal edebilir veya değiştirebilir. İptal
        edilen
        talimat sonrasında yeni bir talimat verilmedikçe, karttan aidat çekimi yapılmayacaktır.</p>

    <strong style="margin-bottom:5px">Madde 6 - Kişisel Verilerin Korunması ve Gizlilik</strong>
    <p>Sendika, üyelerinin kişisel verilerini ve finansal bilgilerini 6698 sayılı Kişisel Verilerin Korunması Kanunu
        (KVKK)
        kapsamında işlemeyeceğini, saklamayacağını ve üçüncü kişilerle paylaşmayacağını taahhüt eder. Üyelerin kişisel
        bilgileri, yalnızca ödeme işlemlerinin gerçekleştirilmesi amacıyla PAYTR sistemi tarafından kullanılacaktır.</p>

    <strong style="margin-bottom:5px">Madde 7 - Sorumluluk Reddi</strong>
    <p>Sendika, kredi/banka kartı bilgilerini işlememekte ve saklamamaktadır. Ödeme süreci PAYTR ödeme altyapısı tarafından
        güvenli bir şekilde yönetilmektedir.</p>

    <strong style="margin-bottom:5px">Madde 8 - Yürürlük</strong>
    <p>İşbu sözleşme, üyelerin online aidat ödeme sistemi üzerinden <strong>üyeye gönderilecek SMS kodunu sisteme
            girerek
            talimat vermesi</strong> ile birlikte yürürlüğe girer. Üyeler, sistemi kullanarak bu sözleşme hükümlerini
        kabul
        etmiş sayılırlar.</p>

    <strong style="margin-bottom:5px">Madde 9 - Değişiklik Hakkı</strong>
    <p>Sendika, işbu sözleşmede dilediği zaman değişiklik yapma hakkını saklı tutar. Yapılacak değişiklikler üyelere
        sistem
        üzerinden bildirilir ve üyeler değişiklik sonrası sistemi kullanmaya devam etmeleri halinde bu değişiklikleri
        kabul
        etmiş sayılırlar.</p>

    <strong style="margin-bottom:5px">Madde 10 - Yürürlük Süresi ve Fesih</strong>
    <p>Bu sözleşme, süresiz olarak yürürlükte kalacaktır. Üye, dilediği zaman sistem üzerinden ödeme talimatını iptal
        ederek
        sözleşmeyi feshedebilir.</p>

    <strong style="margin-bottom:5px">Madde 11 - Uyuşmazlıkların Çözümü</strong>
    <p>İşbu sözleşmeden doğabilecek uyuşmazlıklar Türk Hukuku'na tabi olup, İstanbul Mahkemeleri yetkili
        kılınmıştır.</p>

